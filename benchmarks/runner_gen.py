from __future__ import annotations

import json
import re


_C_IDENTIFIER_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")

_C_KEYWORDS = {
    "auto",
    "break",
    "case",
    "char",
    "const",
    "continue",
    "default",
    "do",
    "double",
    "else",
    "enum",
    "extern",
    "float",
    "for",
    "goto",
    "if",
    "inline",
    "int",
    "long",
    "register",
    "restrict",
    "return",
    "short",
    "signed",
    "sizeof",
    "static",
    "struct",
    "switch",
    "typedef",
    "union",
    "unsigned",
    "void",
    "volatile",
    "while",
    "_Alignas",
    "_Alignof",
    "_Atomic",
    "_Bool",
    "_Complex",
    "_Generic",
    "_Imaginary",
    "_Noreturn",
    "_Static_assert",
    "_Thread_local",
}


def _is_plausible_c_identifier(name: str) -> bool:
    if not isinstance(name, str) or not name:
        return False
    if not _C_IDENTIFIER_RE.match(name):
        return False
    return name not in _C_KEYWORDS


def _c_string_literal_from_bytes(data: bytes) -> str:
    # Use fixed-width octal escapes for any non-printable bytes to avoid greedy
    # `\x..` parsing issues in C.
    out: list[str] = ['"']
    for b in data:
        if b == 0x22:  # "
            out.append(r"\"")
        elif b == 0x5C:  # \
            out.append(r"\\")
        elif b == 0x0A:
            out.append(r"\n")
        elif b == 0x0D:
            out.append(r"\r")
        elif b == 0x09:
            out.append(r"\t")
        elif 0x20 <= b <= 0x7E:
            out.append(chr(b))
        else:
            out.append(f"\\{b:03o}")
    out.append('"')
    return "".join(out)


def _c_string_literal(s: str) -> str:
    if not isinstance(s, str):
        raise TypeError("expected str")
    return _c_string_literal_from_bytes(s.encode("utf-8"))


def generate_benchmark_runner(
    *,
    model_name: str,
    workload_batch_sizes: list[int],
    warmup_iterations: int,
    measure_iterations: int,
    entry_point: str,
    has_constants: bool = False,
    input_tensor_symbols: list[str] | None = None,
) -> str:
    """
    Generate a dedicated C benchmark runner for an NNC-generated model.

    The runner is intended to be compiled alongside the generated C artifacts
    (e.g. model.c/tensors.c) and will emit machine-readable JSON to stdout.
    """
    if not isinstance(model_name, str) or not model_name:
        raise ValueError("model_name must be a non-empty string")
    if not workload_batch_sizes:
        raise ValueError("workload_batch_sizes must be a non-empty list of positive ints")
    if any((not isinstance(x, int)) or x <= 0 for x in workload_batch_sizes):
        raise ValueError("workload_batch_sizes must be a non-empty list of positive ints")
    if not isinstance(warmup_iterations, int) or warmup_iterations < 0:
        raise ValueError("warmup_iterations must be a non-negative int")
    if not isinstance(measure_iterations, int) or measure_iterations < 0:
        raise ValueError("measure_iterations must be a non-negative int")
    if not isinstance(entry_point, str) or not entry_point:
        raise ValueError("entry_point must be a non-empty string")
    if not _is_plausible_c_identifier(entry_point):
        raise ValueError(f"entry_point must be a valid C identifier, got: {entry_point!r}")

    if input_tensor_symbols is None:
        input_tensor_symbols = ["tensor_input"]
    if not isinstance(input_tensor_symbols, list) or not input_tensor_symbols:
        raise ValueError(
            "input_tensor_symbols must be a non-empty list of C identifiers (or None)"
        )
    for sym in input_tensor_symbols:
        if not _is_plausible_c_identifier(sym):
            raise ValueError(
                "input_tensor_symbols must be a non-empty list of valid C identifiers; "
                f"got invalid symbol: {sym!r}"
            )

    workloads_init = ", ".join(str(x) for x in workload_batch_sizes)

    constants_load = ""
    if has_constants:
        constants_load = r"""
    /* Load constants from binary file */
    if (nnc_load_constants("constants.bin") != 0) {
        fprintf(stderr, "Failed to load constants from constants.bin\n");
        return 1;
    }
"""

    # NOTE: For safety, do not splice `model_name` directly into C string literals.
    # We embed a JSON string literal and print it with `%s`.
    model_json_string = json.dumps(model_name)
    model_json_c_literal = _c_string_literal(model_json_string)

    init_inputs_lines: list[str] = []
    for idx, sym in enumerate(input_tensor_symbols):
        seed = 123 + (idx * 1000)
        init_inputs_lines.append(
            f"    init_tensor_f32_deterministic(&{sym}, {seed}u);"
        )
    init_inputs_code = "\n".join(init_inputs_lines) if init_inputs_lines else "    (void)0;"

    return f"""/* Auto-generated by NNC benchmarks - DO NOT EDIT */
#define _POSIX_C_SOURCE 200809L
#include <stdint.h>
#include <errno.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>

#include "nnc_types.h"
#include "model.h"

static int now_ns(uint64_t *out_ns) {{
    struct timespec ts;
    if (clock_gettime(CLOCK_MONOTONIC, &ts) != 0) {{
        return -1;
    }}
    *out_ns = ((uint64_t)ts.tv_sec * 1000000000ull) + (uint64_t)ts.tv_nsec;
    return 0;
}}

static uint32_t lcg_next_u32(uint32_t *state) {{
    /* Deterministic, portable pseudo-random generator. */
    *state = (*state * 1664525u) + 1013904223u;
    return *state;
}}

static void init_tensor_f32_deterministic(Tensor *t, uint32_t seed) {{
    if (!t || !t->data) {{
        return;
    }}
    if (t->dtype != NNC_DTYPE_FLOAT32) {{
        return;
    }}

    uint32_t state = seed;
    size_t n = (size_t)(t->nbytes / 4);
    float *p = (float *)t->data;
    for (size_t i = 0; i < n; i++) {{
        /* Map low 24 bits to [0, 1). */
        uint32_t r = lcg_next_u32(&state);
        p[i] = (float)(r & 0x00FFFFFFu) / (float)0x01000000u;
    }}
}}

static void init_deterministic_inputs(void) {{
    /* Deterministic initialization for configurable input tensor symbols. */
{init_inputs_code}
}}

int main(void) {{
    /* Make stdout deterministic for harness parsing. */
    setvbuf(stdout, NULL, _IONBF, 0);

    static const char* model_json = {model_json_c_literal};

{constants_load}
    init_deterministic_inputs();

    int workloads[] = {{{workloads_init}}};
    int num_workloads = (int)(sizeof(workloads) / sizeof(workloads[0]));

    /* Collect measurements first; emit JSON only after all measurements succeed. */
    /* Collect measurements */
    int *batch_sizes = (int *)malloc((size_t)num_workloads * sizeof(int));
    double *throughputs = (double *)malloc((size_t)num_workloads * sizeof(double));
    size_t lat_samples_count = (size_t)num_workloads * (size_t){measure_iterations};
    double *lat_samples = NULL;
    if (lat_samples_count > 0) {{
        lat_samples = (double *)malloc(lat_samples_count * sizeof(double));
    }}
    if (!batch_sizes || !throughputs || (lat_samples_count > 0 && !lat_samples)) {{
        fprintf(stderr, "Failed to allocate measurement buffers\\n");
        free(batch_sizes);
        free(throughputs);
        free(lat_samples);
        return 1;
    }}

    for (int workload_idx = 0; workload_idx < num_workloads; workload_idx++) {{
        int workload_batch = workloads[workload_idx];
        batch_sizes[workload_idx] = workload_batch;

        /* Warmup: same batch semantics as measurement, but not timed. */
        for (int warmup = 0; warmup < {warmup_iterations}; warmup++) {{
            for (int batch_iter = 0; batch_iter < workload_batch; batch_iter++) {{
                {entry_point}();
            }}
        }}

        uint64_t run_start_ns = 0;
        if (now_ns(&run_start_ns) != 0) {{
            int err = errno;
            fprintf(stderr, "clock_gettime(CLOCK_MONOTONIC) failed: %s\\n", strerror(err));
            free(batch_sizes);
            free(throughputs);
            free(lat_samples);
            return 1;
        }}
        for (int iter = 0; iter < {measure_iterations}; iter++) {{
            uint64_t iter_start_ns = 0;
            if (now_ns(&iter_start_ns) != 0) {{
                int err = errno;
                fprintf(stderr, "clock_gettime(CLOCK_MONOTONIC) failed: %s\\n", strerror(err));
                free(batch_sizes);
                free(throughputs);
                free(lat_samples);
                return 1;
            }}
            for (int batch_iter = 0; batch_iter < workload_batch; batch_iter++) {{
                {entry_point}();
            }}
            uint64_t iter_end_ns = 0;
            if (now_ns(&iter_end_ns) != 0) {{
                int err = errno;
                fprintf(stderr, "clock_gettime(CLOCK_MONOTONIC) failed: %s\\n", strerror(err));
                free(batch_sizes);
                free(throughputs);
                free(lat_samples);
                return 1;
            }}
            double iter_elapsed_ms = (double)(iter_end_ns - iter_start_ns) / 1000000.0;
            double per_sample_ms = iter_elapsed_ms / (double)workload_batch;
            lat_samples[(size_t)workload_idx * (size_t){measure_iterations} + (size_t)iter] = per_sample_ms;
        }}
        uint64_t run_end_ns = 0;
        if (now_ns(&run_end_ns) != 0) {{
            int err = errno;
            fprintf(stderr, "clock_gettime(CLOCK_MONOTONIC) failed: %s\\n", strerror(err));
            free(batch_sizes);
            free(throughputs);
            free(lat_samples);
            return 1;
        }}

        double run_elapsed_s = (double)(run_end_ns - run_start_ns) / 1000000000.0;
        double throughput_samples_per_sec = 0.0;
        if (run_elapsed_s > 0.0) {{
            throughput_samples_per_sec = ((double){measure_iterations} * (double)workload_batch) / run_elapsed_s;
        }}
        throughputs[workload_idx] = throughput_samples_per_sec;
    }}

    /* Emit JSON */
    /* Machine-readable JSON output includes: "runs": [ ... ] */
    printf("{{\\"model\\":%s,\\"runs\\": [", model_json);
    for (int workload_idx = 0; workload_idx < num_workloads; workload_idx++) {{
        if (workload_idx > 0) {{
            printf(",");
        }}
        printf("{{\\"batch_size\\":%d,\\"latency_ms_samples\\":[", batch_sizes[workload_idx]);
        for (int iter = 0; iter < {measure_iterations}; iter++) {{
            if (iter > 0) {{
                printf(",");
            }}
            printf("%.6f", lat_samples[(size_t)workload_idx * (size_t){measure_iterations} + (size_t)iter]);
        }}
        printf("],\\"throughput_samples_per_sec\\":%.6f}}", throughputs[workload_idx]);
    }}
    printf("]}}\\n");

    free(batch_sizes);
    free(throughputs);
    free(lat_samples);
    return 0;
}}
"""
